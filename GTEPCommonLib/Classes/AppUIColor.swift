import Foundation
import UIKit

public extension UIColor {


    @nonobjc class var gtSoftBlue: UIColor {
        return UIColor(red: 0, green: 0.525, blue: 0.792, alpha: 1)
    }
    
    
    @nonobjc class var gtBlue: UIColor {
        return UIColor(red: 6.0 / 255.0, green: 87.0 / 255.0, blue: 163.0 / 255.0, alpha: 1.0)
    }
    
    
    @nonobjc class var gtGreen: UIColor {
