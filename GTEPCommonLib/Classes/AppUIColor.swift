    }
    
    @nonobjc class var gtOrange: UIColor {
        return  UIColor(red: 0.942, green: 0.563, blue: 0.337, alpha: 1)
    }
    
    @nonobjc class var gtYellow: UIColor {
        return UIColor(red: 252.0 / 255.0, green: 191.0 / 255.0, blue: 49.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var gtLightGrey: UIColor {
